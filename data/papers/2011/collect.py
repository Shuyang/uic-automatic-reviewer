	u = urllib2.urlopen('http://aclweb.org/anthology-new/P/P11/P11-'+ str(i)+ '.pdf')
	localFile = open('P11_'+ str(i) + '.pdf','wb')
	localFile.write(u.read())
	localFile.close()
	print('P11'+ str(i) + '.pdf')
	time.sleep(random.randrange(5000)/1000+0.5)


