import urllib2
import time
import random
for i in range(3601,3627):
	u = urllib2.urlopen('http://aclweb.org/anthology-new/W/W12/W12-'+ str(i)+ '.pdf')
	localFile = open('W12_'+ str(i) + '.pdf','wb')
	localFile.write(u.read())
	localFile.close()
	print('fullpaper_'+ str(i) + '.pdf')
	time.sleep(random.randrange(5000)/1000+0.5)

